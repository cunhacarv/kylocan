        <h2 style="text-align:left">O que é</h2>
        <p>O <span style="font-weight:900">Google Glass</span> é um acessório em forma de óculos que possibilita a interação dos usuários com diversos
        conteúdos em realidade aumentada. Também chamado de <a href="http://glass.google.com" target="_blank">Project Glass</a>, o eletrônico é capaz de tirar fotos a partir
        de comandos de voz, enviar mensagens instantâneas e realizar vídeoconferências. Seu lançamento está previsto
        para 2014, e seu preço deve ser de US$ 1,5 mil. Atualmente o Google Glass encontra-se em fase de testes e já
        possui um vídeo totalmente gravado com o dispositivo. Além disso, a companhia de buscas registrou novas
        patentes anti-furto e de desbloqueio de tela para o acessório.</p>
